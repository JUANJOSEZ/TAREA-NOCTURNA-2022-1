package app;

public class ejercicio29 {

    public static void main(String[] args) {
        int n = 0;
        for (int i = 1; i <= 10; i++) {
            for (int k = 1; k <= 12; k++) {
                n = i * k;
                System.out.println(i + "*" + k + "=" + n);
            }
        }
    }
}
